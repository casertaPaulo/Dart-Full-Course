main() {
  // Data Types
  // Numbers, Strings, Booleans, Lists, Maps, Dynamic

  // String
  String name = "Paulo";
  print(name);

  // Integer
  int myNum = 17;
  print('Integer: $myNum');

  // Double
  double pointNumber = 17.17;
  print('Double: $pointNumber');

  // Boolean
  bool isMale = true;
  print('Boolean: $isMale');

  // Dynamic
  dynamic fullName = 'Paulo Henrique';
  print('Dynamic: $fullName');
}
